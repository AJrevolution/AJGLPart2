#include "RenderHelper.h"

unsigned int RenderHelper::cubeVAO = 0;
unsigned int RenderHelper::cubeVBO = 0;
unsigned int RenderHelper::quadVAO = 0;
unsigned int RenderHelper::quadVBO = 0;

void RenderHelper::init()
{
    setupCube();
    setupQuad();
}

void RenderHelper::cleanup()
{
    glDeleteVertexArrays(1, &cubeVAO);
    glDeleteBuffers(1, &cubeVBO);
    glDeleteVertexArrays(1, &quadVAO);
    glDeleteBuffers(1, &quadVBO);
}

void RenderHelper::renderCube()
{
    if (cubeVAO == 0)
    {
        setupCube();
    }

    glBindVertexArray(cubeVAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
}

void RenderHelper::renderQuad()
{
    if (quadVAO == 0)
    {
        setupQuad();
    }

    glBindVertexArray(quadVAO);
    glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
    glBindVertexArray(0);
}

